# 전치행렬(tanspose matrix): t()
# 대각행렬 기준으로 위치 맞바꾸기 그냥 행과 열 숫자 맞바꾸기
# (2, 1) -> (1, 2)
mat1 <- matrix(1:6, 2)
print(mat1)
#       [,1] [,2] [,3]
# [1,]    1    3    5
# [2,]    2    4    6

mat_1t <- t(mat1)
print(mat_1t)
#       [,1] [,2]         
# [1,]    1    2   
# [2,]    3    4
# [3,]    5    6

# 대각 요소 제외 나머지 값들 대칭
print(mat1 %*% mat_1t)
#       [,1] [,2]
# [1,]   35   44
# [2,]   44   56
print(mat_1t %*% mat1)
