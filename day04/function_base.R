# rep(value, amount) : 전달된 value 값을 amount만큼 반복하여
# 벡터를 반환
# each 사용하면 벡터의 각 요소 묶어서 반환
print(rep(c(1,2),5))  # [1] 1 2 1 2 1 2 1 2 1 2
print(rep(c(1,2), each=2)) # [1] 1 1 2 2

